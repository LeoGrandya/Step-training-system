def get_display_name(name: str) -> str:
    return DISPLAY_NAME_MAP.get(name, name)


def rename_columns_for_display(df):
    if df is None:
        return df
    return df.rename(columns={c: get_display_name(c) for c in df.columns})


def sanitize_filename(name: str) -> str:
    name = get_display_name(name)
    name = name.replace("/", "每").replace("\\", "_")
    name = name.replace(":", "：")
    name = name.replace("*", "×")
    name = name.replace("?", "？")
    name = name.replace('"', "”")
    name = name.replace("<", "《").replace(">", "》")
    name = name.replace("|", "_")
    name = re.sub(r"\s+", "", name)
    return name
